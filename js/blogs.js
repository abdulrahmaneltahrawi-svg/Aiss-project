<strong>المخاطر الناجمة عن تجاوز نطاق التخصص:
</strong>
           <br>

      <p>إنَّ محاولة أخصائي السلامة التصدِّي لمشكلات الصحة النفسية للعاملين دون التأهيل المطلوب قد تُفْضي إلى عواقب سلبيَّة متعددة الأوجه:
أولًا: من منظور صحة العامل: قد يؤدِّي التدخل غير المؤهَّل إلى تقديم تقييمات، أو نصائح خاطئة تفاقم حالة الموظف بدل تحسينها، فمثلًا: قد يسيء مسؤول السلامة تقدير خطورة حالة الاكتئاب أو القلق لدى أحد العاملين، فيتعامل معها باستخفاف، أو يُقدِّم حلولًا سطحيةً ليست قائمةً على أسس علمية؛ ممَّا قد يدفع العامل إلى مزيدٍ من العزلة، أو يُثْنيه عن طلب المساعدة المتخصصة.
ثانيًا: هناك مخاطر تتعلَّق بالسريَّة والثقة؛ فالعامل الذي يشارك مشكلاته النفسيَّة مع شخصٍ غير مؤهل قد لا يَحْظى بحماية الخصوصية الواجبة، أو بالتفهُّم العلاجي الصحيح؛ ممَّا قد يزعزع ثقته بمنظومة الدعم في مكان العمل.
ثالثًا: من الناحية المهنية والتنظيمية، قد يتسبَّب تجاوز أخصائي السلامة لدوره في تضارب الصلاحيات مع قسم الموارد البشرية، أو وحدة الرعاية الصحية المهنية في المؤسسة، وهذا التضارب قد يُرْبك قنوات الدعم المتاحة، ويؤدِّي إلى إهمال بعض الحالات، أو تكرار غير فعَّال للجهود.
رابعًا: هناك جانب قانونـي وأخلاقي مهم، فتقديم المشورة، أو الخدمات النفسية بشكلٍ غير مرخص قد يُعرِّض الشركة وأخصائي السلامة لمسؤولية قانونية إذا ما أُصِيبَ الموظف بضرر نتيجة نصائح غير سليمة، ويشير بعض التقارير إلى أن غياب التأهيل والتدريب الملائم يجعلان المديرين ومسؤولي السلامة غير مُسْتعدِّين للتعامل مع قضايا الصحة النفسية لدى الموظفين؛ ممَّا يعني أن محاولاتهم قد تأتـي بنتائج عكسية، وقد حذَّر متخصصون في مجال الصحة النفسية من أن الاعتماد على أفرادٍ غير مُؤهَّلين في تقديم الدعم النفسي قد يُسبِّب ضررًا أكثر ممَّا ينفع في بعض الحالات الحسَّاسة؛ نظرًا لعدم إلمامهم بالأساليب العلاجيَّة الصحيحة، وحدود التدخُّل الآمن، وبالتالي: فإنَّ تجاوز أخصائي السلامة لدوره المرسوم لا يُخلُّ بمبدأ التخصصية فحَسْب، بل قد يُشكِّل في حدِّ ذاته خطرًا مهنيًّا على صحة العامل النفسية، وعلى سُمْعة المنظمة والتزامها برعاية مُوظَّفيها.
ومن هنا تَبْرز ضرورة وَضْع بروتوكولات واضحة في بيئات العمل تُحدِّد متى يجب على مسؤول السلامة تسليم زمام الأمور إلى المختصِّين النفسيِّين، وضمان وجود قنوات إحالة ودعم محترفة، وسهلة الوصول للعاملين.
</p>
